    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String placa = txtplaca.getText().trim();
    String modelo = txtmodelo.getText().trim();
    String capacidad = spinner1.getValue().toString(); // Assuming spinner1 is a JSpinner

    if (placa.isEmpty() || modelo.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingresa los datos correctamente");
    } else {
        try {
            String consulta = "INSERT INTO autobus(placa, modelo, capacidad) VALUES (?, ?, ?)";
            PreparedStatement ps = cn.prepareStatement(consulta);
            ps.setString(1, placa);
            ps.setString(2, modelo);
            ps.setString(3, capacidad);
            ps.executeUpdate();
            
            limpiar(); // Assuming this clears the form
            JOptionPane.showMessageDialog(null, "Datos Registrados");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se registraron los datos: " + e.getMessage());
        }
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

