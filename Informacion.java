    private void cargarDatosAutobus() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String url  = "jdbc:mysql://localhost:3307/registro";
        String usr  = "root";
        String pwd  = "";
        String sql  = "SELECT placas, modelo, capacidad FROM informacion";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection con = DriverManager.getConnection(url, usr, pwd);
                 Statement st  = con.createStatement();
                 ResultSet rs  = st.executeQuery(sql)) {

                while (rs.next()) {
                    String placas    = rs.getString("placas");
                    String modelo    = rs.getString("modelo");
                    int capacidad    = rs.getInt("capacidad");
                    model.addRow(new Object[]{placas, modelo, capacidad});
                }
            }
        } catch (ClassNotFoundException cnf) {
            JOptionPane.showMessageDialog(this,
                "Falta el driver de MySQL:\n" + cnf.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException sqle) {
            JOptionPane.showMessageDialog(this,
                "Error al leer datos de la base:\n" + sqle.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

