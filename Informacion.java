    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

    int filaSeleccionada = jTable1.getSelectedRow();

    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un autobús para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Obtenemos la placa (clave única)
    String placas = jTable1.getValueAt(filaSeleccionada, 0).toString(); // columna 0 = placas

    int confirmar = JOptionPane.showConfirmDialog(this,
        "¿Seguro que deseas eliminar el autobús con placas: " + placas + "?",
        "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

    if (confirmar == JOptionPane.YES_OPTION) {
        String url = "jdbc:mysql://localhost:3307/registro";
        String usr = "root";
        String pwd = "";
        String sql = "DELETE FROM informacion WHERE placas = ?";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection con = DriverManager.getConnection(url, usr, pwd);
                 PreparedStatement ps = con.prepareStatement(sql)) {

                ps.setString(1, placas);
                int filas = ps.executeUpdate();

                if (filas > 0) {
                    JOptionPane.showMessageDialog(this, "Autobús eliminado correctamente.");
                    cargarDatosAutobus(); // Vuelve a cargar la tabla
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró el autobús.", "Error", JOptionPane.ERROR_MESSAGE);
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar:\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    
    }//GEN-LAST:event_jButton2ActionPerformed

