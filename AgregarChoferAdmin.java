    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String nombre=txtnombre.getText();
        String apellidos=txtapellido.getText();
        String tipousuario=combo1.getSelectedItem().toString();
        String numtarjeta=txttarjeta.getText();
        String correo=txtcorreo.getText();
        String contraseña=txtContra.getText();

        if(nombre.isEmpty()||apellidos.isEmpty()||numtarjeta.isEmpty()||correo.isEmpty()||contraseña.isEmpty()){
            JOptionPane.showMessageDialog(null,"Ingresa los datos correctamente");
        }else{
            if(tipousuario.equalsIgnoreCase("Seleccionar")){
                JOptionPane.showMessageDialog(null,"Seleccione un campo valido");
            }
            else{
                try{
                    String consulta="INSERT INTO usuario(nombre, apellidos,tipousuario,numtarjeta,correo,contraseña)"
                    + "values('"+nombre+"','"+apellidos+"','"+tipousuario+"','"+numtarjeta+"','"+correo+"','"+contraseña+"')";
                    PreparedStatement ps=cn.prepareStatement(consulta);
                    ps.executeUpdate();
                    limpiar();
                    JOptionPane.showMessageDialog(null,"Datos Registrados");
                }catch(Exception e){    JOptionPane.showMessageDialog(null,"No se registraron los datos");}
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

