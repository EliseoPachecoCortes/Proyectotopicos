    private void txtContraKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContraKeyReleased
          if(c1 == true){
        String contrasena = txtContra.getText();
        // Validación de carácter especial
    if (validarcontra.validarCaracterEspecial(contrasena)) {
        lblCarEsp.setForeground(new Color(0,153,0));
    } else {
        lblCarEsp.setForeground(new Color(150,48,90));
    }
    
    // Validación de longitud
    if (validarcontra.validarLongitud(contrasena)) {
        lbl8car.setForeground(new Color(0,153,0));
    } else {
        lbl8car.setForeground(new Color(150,48,90));
    }
    
    // Validación de número
    if (validarcontra.validarNumero(contrasena)) {
        lblContNum.setForeground(new Color(0,153,0));
    } else {
        lblContNum.setForeground(new Color(150,48,90));
    }
    
    // Validación de mayúscula
    if (validarcontra.validarMayuscula(contrasena)) {
        lblContMayus.setForeground(new Color(0,153,0));
    } else {
        lblContMayus.setForeground(new Color(150,48,90));
    }
          }
    }//GEN-LAST:event_txtContraKeyReleased

