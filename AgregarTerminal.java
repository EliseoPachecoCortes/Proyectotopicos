    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String nombre=txtnombre.getText();
        String localidad=txtlocalidad.getText();
        String calle=txtcalle.getText();
        String numero=txtnum.getText();
        String codpostal=txtcp.getText();
        

        if(nombre.isEmpty()||localidad.isEmpty()||calle.isEmpty()||numero.isEmpty()||codpostal.isEmpty()){
            JOptionPane.showMessageDialog(null,"Ingresa los datos correctamente");
        }else{
          
            
                try{
                    String consulta="INSERT INTO terminal(nombre, localidad,calle,numero,codpostal)"
                    + "values('"+nombre+"','"+localidad+"','"+calle+"','"+numero+"','"+codpostal+"')";
                    PreparedStatement ps=cn.prepareStatement(consulta);
                    ps.executeUpdate();
                    limpiar();
                    JOptionPane.showMessageDialog(null,"Datos Registrados");
                }catch(Exception e){    JOptionPane.showMessageDialog(null,"No se registraron los datos");}
            }
        
    }//GEN-LAST:event_btnsaveActionPerformed

